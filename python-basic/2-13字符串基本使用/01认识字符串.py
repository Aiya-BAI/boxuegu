a = 'helllo world'
print(a)
print(type(a))

b = 'TO' \
    'M'
print(b)
print(type(b))

#三引号  支持回车换行，并且换行原样输出
c = '''i am
 TOM'''
print(type(c))

d = """i am 
tom"""
print(type(d))
print(d)

# I'm tom  双引号包含单引号所谓
e = "I'm tom"
print(type(e))
print(e)

# I'm tom  转义
f = 'I\'m tom'
print(type(f))
print(f)

